// Pause for start screen
void pause(){
  #if defined _WIN32
    system("pause");
  
  #elif defined (__LINUX__) || defined (__gnu_linux__) || defined (__linux__)
    system("read");

  #elif defined (__APPLE__)
    system("read");

  #endif
}

// Clears terminal window
void clear(){
  #if defined _WIN32
    system("cls");
  
  #elif defined (__LINUX__) || defined (__gnu_linux__) || defined (__linux__)
    system("clear");

  #elif defined (__APPLE__)
    system("clear");

  #endif
}
